	mux.HandleFunc("GET /api/channels/{channelID}/now", a.handleChannelNow)
	mux.HandleFunc("GET /api/channels/{channelID}/media", a.handleChannelMedia)
	mux.HandleFunc("GET /api/channels/{channelID}/filler-assets", a.handleChannelFillerAssets)
	mux.HandleFunc("GET /api/channels/{channelID}/schedule", a.handleChannelSchedule)
